class Blanket {

    String brand;
    String material;
    String color;
    double price;
    double size;
    boolean isWashable;

    Blanket(String brand, String material, String color,
            double price, double size, boolean isWashable) {

        this.brand = brand;
        this.material = material;
        this.color = color;
        this.price = price;
        this.size = size;
        this.isWashable = isWashable;
    }
}