class TennisBall {

    void bounce() { System.out.println("Tennis ball is bouncing"); }
	
    void roll() { System.out.println("Tennis ball is rolling"); }
	
    void spin() { System.out.println("Tennis ball is spinning"); }
	
    void hitWithRacket() { System.out.println("Tennis ball is hit with racket"); }
	
    void serve() { System.out.println("Tennis ball is served"); }
	
    void crossNet() { System.out.println("Tennis ball crossed the net"); }
	
    void landOnCourt() { System.out.println("Tennis ball landed on court"); }
	
    void moveFast() { System.out.println("Tennis ball is moving fast"); }
	
    void slowDown() { System.out.println("Tennis ball is slowing down"); }
	
    void changeDirection() { System.out.println("Tennis ball changed direction"); }
	
    void compressOnImpact() { System.out.println("Tennis ball compressed on impact"); }
	
    void returnShot() { System.out.println("Tennis ball returned by player"); }
	
    void goOutOfBounds() { System.out.println("Tennis ball went out of bounds"); }
	
    void getCollected() { System.out.println("Tennis ball collected by ball boy"); }
	
    void getStored() { System.out.println("Tennis ball stored in box"); }
	
	

    static void showColor() { System.out.println("Tennis ball color is yellow"); }
	
    static void showWeight() { System.out.println("Showing tennis ball weight"); }
	
    static void showSize() { System.out.println("Showing tennis ball size"); }
	
    static void showMaterial() { System.out.println("Showing tennis ball material"); }
	
    static void showBrandTypes() { System.out.println("Showing different tennis ball brands"); }
}