class Missile {

    void launch() { System.out.println("Missile is launched"); }

    void igniteEngine() { System.out.println("Missile engine is ignited"); }

    void accelerate() { System.out.println("Missile is accelerating"); }

    void changeDirection() { System.out.println("Missile changed direction"); }

    void trackTarget() { System.out.println("Missile is tracking the target"); }

    void lockTarget() { System.out.println("Missile locked on target"); }

    void increaseAltitude() { System.out.println("Missile is increasing altitude"); }

    void decreaseAltitude() { System.out.println("Missile is decreasing altitude"); }

    void activateGuidance() { System.out.println("Missile guidance system activated"); }

    void detectObstacle() { System.out.println("Missile detected an obstacle"); }

    void avoidObstacle() { System.out.println("Missile avoided obstacle"); }

    void enterTargetZone() { System.out.println("Missile entered target zone"); }

    void impact() { System.out.println("Missile impacted the target"); }

    void selfDestruct() { System.out.println("Missile self-destruct sequence activated"); }

    void shutdown() { System.out.println("Missile system shutting down"); }


    static void showType() { System.out.println("Missiles can be ballistic or cruise"); }

    static void showRange() { System.out.println("Missiles have short, medium, or long range"); }

    static void showFuelType() { System.out.println("Missiles use solid or liquid fuel"); }

    static void showCountryUsage() { System.out.println("Missiles are used by various countries"); }

    static void showPurpose() { System.out.println("Missiles are used for defense purposes"); }
}