class Racket {

    String brand;
    String type;
    String color;
    double weight;
    double price;
    String material;
    String gripType;
    int length;
    String playerName;
    String country;

    Racket(String brand, String type, String color, double weight, double price,
           String material, String gripType, int length,
           String playerName, String country) {

        this.brand = brand;
        this.type = type;
        this.color = color;
        this.weight = weight;
        this.price = price;
        this.material = material;
        this.gripType = gripType;
        this.length = length;
        this.playerName = playerName;
        this.country = country;
    }
}