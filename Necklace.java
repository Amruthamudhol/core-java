class Necklace {

    String brand;
    String material;
    String color;
    double price;
    double weight;
    String length;
    String type;
    String stoneType;
    String claspType;
    boolean isAdjustable;
    String gender;
    String originCountry;
    String designStyle;
    boolean isHandmade;
    int warrantyYears;
    String occasion;
    String plating;
    boolean isWaterproof;
    int rating;
    String modelNumber;

    Necklace(String brand, String material, String color, double price, double weight,
             String length, String type, String stoneType, String claspType,
             boolean isAdjustable, String gender, String originCountry,
             String designStyle, boolean isHandmade, int warrantyYears,
             String occasion, String plating, boolean isWaterproof,
             int rating, String modelNumber) {

        this.brand = brand;
        this.material = material;
        this.color = color;
        this.price = price;
        this.weight = weight;
        this.length = length;
        this.type = type;
        this.stoneType = stoneType;
        this.claspType = claspType;
        this.isAdjustable = isAdjustable;
        this.gender = gender;
        this.originCountry = originCountry;
        this.designStyle = designStyle;
        this.isHandmade = isHandmade;
        this.warrantyYears = warrantyYears;
        this.occasion = occasion;
        this.plating = plating;
        this.isWaterproof = isWaterproof;
        this.rating = rating;
        this.modelNumber = modelNumber;
    }
}