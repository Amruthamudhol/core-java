class Bracelet {

    String brand;
    String material;
    String color;
    double price;
    double weight;
    String type;
    String size;
    boolean isAdjustable;
    String gender;
    String stoneType;
    String claspType;
    String originCountry;
    String designStyle;
    boolean isHandmade;
    int warrantyYears;
    String occasion;
    String plating;
    boolean isWaterproof;
    int rating;
    String modelNumber;

    Bracelet(String brand, String material, String color, double price, double weight,
             String type, String size, boolean isAdjustable, String gender,
             String stoneType, String claspType, String originCountry,
             String designStyle, boolean isHandmade, int warrantyYears,
             String occasion, String plating, boolean isWaterproof,
             int rating, String modelNumber) {

        this.brand = brand;
        this.material = material;
        this.color = color;
        this.price = price;
        this.weight = weight;
        this.type = type;
        this.size = size;
        this.isAdjustable = isAdjustable;
        this.gender = gender;
        this.stoneType = stoneType;
        this.claspType = claspType;
        this.originCountry = originCountry;
        this.designStyle = designStyle;
        this.isHandmade = isHandmade;
        this.warrantyYears = warrantyYears;
        this.occasion = occasion;
        this.plating = plating;
        this.isWaterproof = isWaterproof;
        this.rating = rating;
        this.modelNumber = modelNumber;
    }
}